package com.coconut.HomeWork;

public class ListNode {
    /**
     * 单链表
     */
    // 类属性，整数val，ListNode next
    int val;
    ListNode next;

    ListNode() {
    }

    ListNode(int val) {
        this.val = val;
    }

    ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}

